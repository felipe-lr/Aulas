public class Exercicio12 {

	public static void main(String[] args) {
		
		/*
		 * codigo que dara o retorno do valor de x enquanto ele for menor que 10
		 * ao vereficar se é menor que 10 ele soma com +1
		 */
		
		
		int x=1;
		
		while (x<10){
			System.out.println(x);
			x= x + 1;
		}
	}
}
